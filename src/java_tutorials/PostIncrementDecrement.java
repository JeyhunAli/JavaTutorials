package java_tutorials;

public class PostIncrementDecrement {

	public static void main(String[] args) {

		int i=1;      //post increment 
		int y=i++; //the value of the i given to the y; this call post increment  
		
		System.out.println(i);
		System.out.println(y);
		
		int a=1;
		int b= ++a; //pre increment:  it means increase the value of a by one first,the value of a is 1 increase it by one it
		            //means plus 1 its equal to 2,two here alreade given to the b  the console will be a=2,b=2.
		
		System.out.println(a);
		System.out.println(b);
		
		int m=1;  //post decrement
		int c= m--;
		System.out.println(m);
		System.out.println(c);
		
		
		int d=5;  //pre decrement 
		int t= --d;
		System.out.println(d);
		System.out.println(t);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
