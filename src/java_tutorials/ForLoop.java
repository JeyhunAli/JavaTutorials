package java_tutorials;

public class ForLoop {
	public static void main(String[] args) {
		
	

	for (int i=0; i<=10; i++  ) {
		System.out.println(i);
		
	}
	int[]numbers= {10,20,30};
	for(int i=0;i<numbers.length;i++) {
		System.out.println("the value of index: " +i+ "is: "+ numbers[i]);
	}
}
}